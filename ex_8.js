//Faça um script para calcular e mostrar a área de um triângulo (área = (base * altura) /2.

let base = parseFloat(prompt("Digite a base do triângulo"));
let altura = parseFloat(prompt("Digite a altura do triângulo"));

area = (base * altura) / 2;

alert(`A área do triângulo de base ${base} e altura ${altura} é ${area}`);
